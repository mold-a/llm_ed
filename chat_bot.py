from yandex_client import YandexGPTClient  # импортируем наш клиент из соседнего файла
import os  # для чтения переменных окружения
from dotenv import load_dotenv  # для загрузки .env файла

load_dotenv()  # читаем .env и загружаем YANDEX_API_KEY и YANDEX_FOLDER_ID в окружение

# создаём клиент — передаём ключи из переменных окружения
client = YandexGPTClient(
    api_key=os.getenv("YANDEX_API_KEY"),
    folder_id=os.getenv("YANDEX_FOLDER_ID")
)

# system prompt задаёт роль и поведение модели на весь диалог
# role: system — особая роль, модель воспринимает это как инструкцию а не как сообщение пользователя
SYSTEM_PROMPT = {
    "role": "system",
    "text": (
        "Ты строгий senior Python разработчик с 10-летним опытом. "
        "Твоя задача — делать code review. "
        "Указывай на все проблемы: стиль, производительность, безопасность, читаемость. "
        "Не хвали без причины. Будь конкретным и прямым. "
        "Если код хороший — скажи об этом кратко и объясни почему. "
        "Отвечай только на английском языке."
    )
}

# история диалога — список куда добавляем сообщения по ходу разговора
# начинаем только с system prompt, сообщений пользователя ещё нет
messages = [SYSTEM_PROMPT]

print("Code Review Bot запущен.")
print("Команды: 'выход' — завершить, 'сброс' — очистить историю")
print("-" * 50)  # разделитель для красоты

# бесконечный цикл — работает пока пользователь не напишет 'выход'
while True:
    user_input = input("Ты: ").strip()  # strip() убирает лишние пробелы по краям

    # проверяем команду выхода — завершаем программу
    if user_input.lower() == "выход":
        print("Завершаю работу.")
        break  # выходим из цикла while True

    # проверяем команду сброса — очищаем историю но оставляем system prompt
    if user_input.lower() == "сброс":
        messages = [SYSTEM_PROMPT]  # возвращаем список к начальному состоянию
        print("История очищена. Начинаем заново.")
        continue  # пропускаем остаток цикла и начинаем следующую итерацию

    # проверяем что пользователь не отправил пустую строку
    if not user_input:
        print("Введи сообщение.")
        continue  # пропускаем пустой ввод

    # добавляем сообщение пользователя в историю
    messages.append({"role": "user", "text": user_input})

    # формируем урезанную историю для отправки в API
    # messages[0] — всегда system prompt, его берём отдельно
    # messages[-6:] — срез: последние 6 сообщений из истории (без system prompt)
    # итого отправляем: system prompt + последние 6 сообщений
    limited_messages = [messages[0]] + messages[1:][-6:]

    # отправляем историю в API и получаем ответ
    # try/except ловит ошибки сети или API чтобы программа не падала
    try:
        response = client.chat(limited_messages)  # возвращает строку с ответом
    except Exception as e:
        # если запрос не удался — выводим ошибку и продолжаем цикл
        print(f"Ошибка запроса: {e}")
        continue

    # добавляем ответ модели в историю — важно чтобы в следующем запросе был контекст
    messages.append({"role": "assistant", "text": response})

    # выводим ответ модели пользователю
    print(f"\nРевьюер: {response}\n")